    public boolean emailValidity() {
        String em = txtEmail.getText().toString();
        if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter Email address");
            txtEmail.requestFocus();
            return true;
        } else {
            if (em.indexOf("@") == -1) {
                JOptionPane.showMessageDialog(rootPane, "Invalid Email");
                 txtEmail.requestFocus();
                return true;
            } else {
                if (em.indexOf(".com") == -1) {
                    JOptionPane.showMessageDialog(rootPane, "Invalid Email");
                     txtEmail.requestFocus();
                    return true;
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Email is Valid");
                    txtID.requestFocus();
                    return true;
                }
            }
        }
    }

