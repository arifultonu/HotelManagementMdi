    public void loadComboR() {
        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;

        try {
            conn = Database.getConnection();
            stmt = conn.createStatement();
            rs = stmt.executeQuery("select room_no from reservation_info order by room_no");

            List plist = new ArrayList();
            while (rs.next()) {
                plist.add(rs.getString("room_no"));
            }
            cbmItem1.setModel(new javax.swing.DefaultComboBoxModel(plist.toArray()));
            cbmItem1.insertItemAt("--Select--", 0);
            cbmItem1.setSelectedIndex(0);
        } catch (Exception e1) {
            e1.printStackTrace();
        }

    }

