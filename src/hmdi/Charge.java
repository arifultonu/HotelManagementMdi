private void cbmRnItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbmRnItemStateChanged
// TODO add your handling code here:
    if (cbmRn.getSelectedIndex() > 0) {
        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;

        try {
            conn = Database.getConnection();
            stmt = conn.createStatement();
            rs = stmt.executeQuery("select * from customer_info where room_no = '" + cbmRn.getSelectedItem().toString() + "'");
            while (rs.next()) {
                txtFN.setText(rs.getString("first_name"));
                txtLN.setText(rs.getString("last_name"));
            }
        } catch (Exception e1) {
            e1.printStackTrace();
        }
    }
}//GEN-LAST:event_cbmRnItemStateChanged

