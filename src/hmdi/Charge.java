    private void loadComboR() {
        Connection conn = null;
        Statement stmt = null;
        ResultSet rs = null;

        try {
            conn = Database.getConnection();
            stmt = conn.createStatement();
            rs = stmt.executeQuery("select room_no from customer_info order by room_no");

            List plist = new ArrayList();
            while (rs.next()) {
                plist.add(rs.getString("room_no"));
            }
            cbmRn.setModel(new javax.swing.DefaultComboBoxModel(plist.toArray()));
            cbmRn.insertItemAt("--Select--", 0);
            cbmRn.setSelectedIndex(0);
        } catch (Exception e1) {
            e1.printStackTrace();
        }

    }

